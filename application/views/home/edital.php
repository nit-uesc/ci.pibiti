		<h4>Edital n° 24/2016</h4>
		<p class="justify">A Reitora da Universidade Estadual de Santa Cruz – UESC, em conformidade com Resolução Normativa N°. 
			017/2006 do Conselho Nacional de Desenvolvimento Científico e Tecnológico (CNPq) com as decisões do Comitê 
			Institucional/NIT UESC e do Comitê Externo/CNPq, torna pública abertura das inscrições para o Programa Institucional
			de Bolsas de Iniciação em Desenvolvimento Tecnológico e Inovação – PIBITI/UESC(2016-2017)
		</p>

		<h4>1. Objetivos</h4>
		<p class="justify">
			1.1. Contribuir para a formação e inserção de estudantes em atividades de pesquisa, 
			desenvolvimento tecnológico e inovação.<br>
			1.2. Contribuir para a formação de recursos humanos que se dedicarão ao 
			fortalecimento da capacidade inovadora das empresas no País.<br>
			1.3. Contribuir para a formação do cidadão pleno, com condições de participar de 
			forma criativa e empreendedora na sua comunidade.<br>
			1.4. Estimular o desenvolvimento tecnológico e a criatividade decorrentes das 
			condições criadas pelo confronto direto das necessidades de mercado com os 
			problemas de pesquisa, em conformidade com a definição de inovação tecnológica 
			constante no “Manual de Oslo”, a saber:
			“A inovação é definida como a implementação de um produto (bem ou 
			serviço) novo ou significativamente melhorado, ou um processo, ou 
			um novo método de marketing, ou um novo método organizacional nas 
			práticas de negócios, na organização do local de trabalho ou nas relações 
			externas.” (íntegra disponível em: <a href=" http://www.uesc.br/nucleos/nit">http://www.uesc.br/nucleos/nit</a>).
		</p>

		<h4>2. Bolsa</h4>

		<p class="justify">
			2.1. O presente Edital visa a distribuição de bolsas PIBITI/UESC, que tem como fonte pagadora CNPQ e UESC.<br>				
			2.2. A bolsa terá vigência de até 12 (doze) meses, com início previsto em 1º de setembro de 2016 e término em 31 de agosto de 2017, 
			não podendo ultrapassar a data da finalização da graduação, quando se tratar de bolsista formando.<br>
			2.3. As bolsas de Iniciação em Desenvolvimento Tecnológico e Inovação serão 
			atribuídas preferencialmente aos orientadores que já desenvolvam projetos na área 
			tecnológica.<br>
			2.4. A bolsa não é acumulável com estágios remunerados, bolsa de outros programas 
			e/ou empregos de qualquer natureza. Tem caráter transitório e não configura vínculo 
			empregatício, exceto as bolsas de Auxílio Moradia, Auxílio Permanência e correlatos.
		</p>

