		<h4>4. Requisitos, Compromissos e Direitos do Orientador</h4>

		<p class="justify">
			4.1. Ser docente, pesquisador ou bolsista de pesquisa (PRODOC, DTR, DCT, DCR) da UESC em regime de Tempo Integral ou Dedicação Exclusiva, estar ativo no momento de implementação da bolsa de IT e durante toda a sua vigência.<br>
			4.2. Possuir experiência em atividades de geração e transferência de tecnologia.<br>
			4.3. Quanto ao recebimento das bolsas, os pesquisadores de reconhecida 
			competência científica/tecnológica deverão ter precedência em relação aos demais. 
			Bolsistas de Produtividade em Pesquisa e em Desenvolvimento Tecnológico e 
			Extensão Inovadora do CNPq, por definição, têm reconhecida competência científica/
			tecnológica.<br>
			4.4. Cabe ao orientador escolher e indicar, para bolsista, o aluno com perfil e 
			desempenho acadêmicos compatíveis com as atividades previstas observando 
			princípios éticos e conflito de interesse.<br>
			4.5. O orientador poderá, com justificativa, solicitar a exclusão de um bolsista, 
			podendo indicar novo aluno para a vaga, desde que satisfeitos os prazos operacionais 
			adotados pela instituição.<br>
			4.6. O pesquisador deverá incluir o nome do bolsista nas publicações e nos trabalhos 
			apresentados em congressos e seminários, cujos resultados tiveram a participação 
			efetiva do bolsista.<br>
			4.7. É vedada a divisão da mensalidade de uma bolsa entre dois ou mais alunos.<br>
			4.8. O orientador e o bolsista deverão participar do curso de Introdução à Proteção 
			Intelectual com duração de 4 horas, para efetivação da bolsa, a ser ministrado em data 
			a ser definida pelo NIT – UESC, ou outro que o NIT reconheça como equivalente.<br>
			4.9. Entrega do Relatório Semestral (parcial) e Final seguindo o roteiro a ser 
			disponibilizado e data a ser estabelecida pelo NIT da UESC.<br>
			4.10. Confecção de resumo e participação do orientador e do bolsista no Congresso 
			Acadêmico PIBITI em data a ser estabelecida pelo NIT da UESC.<br>
			4.11. O professor participante como orientador PIBITI deve atuar como assessor “ad 
			hoc” no Programa quando solicitado, se não houver a colaboração o mesmo será 
			excluído da lista PIBITI, bem como não poderá aplicar solicitações.<br>
			4.12. A não apresentação do Resumo e do Relatório Final implica no cancelamento da 
			bolsa para trabalhos em andamento, e pleito futuro no Programa.<br>
		</p>

		<h4>5. Requisitos e Compromissos do Bolsista</h4>

		<p class="justify">
			5.1. Estar regularmente matriculado na UESC.<br>

			5.2. Possuir currículo cadastrado na plataforma Lattes do CNPq (http://lattes.cnpq.br/).<br>
			5.3. Não ter vínculo empregatício e dedicar-se integralmente às atividades do plano de 
			trabalho proposto.<br>
			5.4. Ser selecionado e indicado pelo orientador.<br>
			5.5. Executar o Plano de Trabalho aprovado, sob supervisão de seu orientador.<br>
			5.6. Apresentar, após 06 (seis) meses de vigência do Programa, relatório das 
			atividades desenvolvidas no período, contemplando os resultados já alcançados e 
			permitindo constatar seu desempenho naquele período.<br>
			5.7. Apresentar no evento a ser designado pelo NIT a produção tecnológica, sob a forma de 
			pôsteres, resumos e/ou painéis.<br>
			5.8. Nas publicações e trabalhos apresentados, fazer referência a sua condição de 
			bolsista do CNPq ou UESC.<br>
			5.9. Não receber outra modalidade de bolsa dentro dos programas da UESC ou de outra instituição, 
			exceto as bolsas de Auxílio Moradia, Auxílio Permanência e correlatos.<br>
			5.10. Comunicar imediatamente ao orientador quaisquer impedimentos ou 
			desinteresse à continuidade de sua condição de bolsista do programa.<br>
			5.11. Devolver à entidade financiadora, em valores atualizados, a(s) mensalidade(s) recebida(s) indevidamente, 
			caso os requisitos e compromissos estabelecidos acima não sejam cumpridos.<br>
			5.12. Participar do curso de Introdução à Proteção Intelectual, com duração de 4 
			horas, para efetivação da bolsa, a ser ministrado em data a ser definida pelo NIT – 
			UESC, ou outro que o NIT reconheça como equivalente.<br>
			5.13. O não cumprimento dos requisitos do item 5 implica o desligamento automático 
			do bolsista do Programa de Iniciação em Desenvolvimento Tecnológico e Inovação da 
			UESC.<br>
		</p>

		<h4>6. O projeto</h4>
		<p class="justify">6.1. O projeto deverá pertencer à área de atuação do Orientador, preferencialmente, associado às linhas de pesquisa do Departamento e/ou do Programa de Pós-Graduação. <br>
			6.2. O projeto deverá ter mérito tecnológico ou relevância do ponto de vista dos temas  Propriedade Intelectual e Transferência de Tecnologia.<br>
			6.3. O projeto deverá ter viabilidade técnica e econômica. O orientador deverá citar os financiamentos de pesquisa por Agências de Fomento e/ou outras Instituições, ou demonstrar não haver necessidade.<br>
			6.4. O projeto deverá ter no máximo 10 (dez) páginas e ser apresentado obedecendo, no que couber, ao seguinte roteiro mínimo: Título do projeto; Resumo; Justificativa; Objetivos; Resultado da Revisão Patentária/Busca de Anterioridade; Metodologia; Resultados Esperados, Infraestrutura Disponível, Impactos Previstos e Referências Bibliográficas.<br>
			6.5. O projeto deverá conter Plano de Trabalho detalhado e individualizado com respectivo cronograma de execução para cada bolsa a ser implementada.<br>
