		<h4>10. Disposições Gerais</h4>

		<p class="justify">10.1. A submissão da solicitação implica o aceite automático de todos os termos deste Edital e das normas específicas do CNPq.<br>
			10.2. Não serão analisadas solicitações de orientadores com pendências junto o NIT ou a PROPP/UESC, conforme Resolução CONSEPE/UESC N° 23/2008. <br>
			10.3. O resultado final da seleção será divulgado pelo Comitê Institucional/NIT - UESC na página do NIT (http://nit.uesc.br/pibiti2016). Esclarecimentos sobre este Edital e informações adicionais sobre o PIBITI/UESC, poderão ser obtidas no NIT/UESC, pelo telefone (73) 3680-5392, pelo e-mail nit@uesc.br.<br>
			10.4. Para as propostas aprovadas que envolvam pesquisas com seres humanos, com organismos geneticamente modificados ou com animais, o orientador deverá encaminhar ao NIT o nº de protocolo de solicitação de parecer ao Comitê de Ética correspondente ou o parecer de aprovação. A não apresentação dessa documentação resultará em não implementação da bolsa.<br>
			10.5. O número total de bolsas, ainda a ser definido pelas agências de fomento e/ou UESC, será distribuído pela classificação geral da proposta aprovada observado a prioridade de um bolsista (plano de trabalho) por projeto qualificado segundo o item 8.5. Entre a possibilidade de deixar um orientador com dois bolsistas ou atribuir uma primeira bolsa a outro orientador, esta última opção terá prioridade.<br>
			10.6. Em caso de concessão de bolsas adicionais por parte do CNPq, da UESC ou outra Instituição de Fomento, para a vigência 2016/2017 estas serão distribuídas de acordo com normas locais e com as recomendações do financiador, respeitando o Banco de Reserva.<br>
			10.7. Os casos omissos serão analisados e decididos pelo Comitê Institucional/NIT - UESC.<br>
