<h3>SOBRE O PROGRAMA</h3>
<div class="subtitle">
	<p class="justify">O Programa Institucional de Bolsas de Iniciação em Desenvolvimento Tecnológico e Inovação (PIBITI) tem por objetivo estimular os jovens do ensino superior nas atividades, metodologias, conhecimentos e práticas próprias ao desenvolvimento tecnológico e processos de inovação.</p>
</div>
<h5>DURAÇÃO DA BOLSA</h5>
<p class="justify">A bolsa terá duração de 12 (doze) meses.</p>
<h5>BOLSA</h5>
<p class="justify">A bolsa de Iniciação Tecnológica para o estudante de graduação.</p>
<h5>INSTITUIÇÕES</h5>
<ul>
	<li>
		<p class="justify">
			Requisitos: instituições de ensino e/ou pesquisa (públicas, privadas, confessionais e (comunitárias); que atuam na área tecnológica
		e de inovação; que mantêm comprovada interação com empresas e/ou com a comunidade, e cujos bolsistas participem de projetos vinculados a
		empresas e/ou organizações.
		</p>
	</li>
	<li><p class="justify">Condições de participação: a Chamada Pública de propostas para o processo de inscrição ocorre no primeiro semestre de
		cada ano, em geral, entre os meses de março e abril.</p>
	</li>
</ul>
<h5>PESQUISADOR</h5>
<ul>
	<li>
		<p class="justify">Requisitos para o orientador: estar vinculado à  instituição de Ensino e/ou Pesquisa que participe do PIBITI;
		desenvolver pesquisa científica, e ser, preferencialmente, bolsista de Produtividade em Pesquisa do CNPq.</p>
	</li>
	<li><p class="justify">Condições de participação: participar em processo de seleção realizado em sua instituição de vínculo.
		Os processos de seleção nas instituições ocorrem, em geral, no primeiro semestre de cada ano. Esteja atento aos
		prazos estipulados em sua instituição.</p>
	</li>
</ul>
<h5>ESTUDANTES</h5>
<p class="justify">
	Requisitos: cursar graduação, e dedicar-se integralmente às atividades acadêmicas e de pesquisa.
	Condições de participação: procure, em sua área de interesse, um pesquisador que esteja disposto a integrá-lo em sua pesquisa e a orientá-lo.
</p>
