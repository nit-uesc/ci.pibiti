<h3>CONTRATAÇÃO DE BOLSISTAS</h3>
<p class="justify">O bolsista indicado pelo orientador através de Comunicação Interna endereçada ao NIT deverá entregar a documentação completa no período estabelecido e informado pelo NIT. Em caso de não entrega na documentação o aluno será desclassificado, sendo convocado o banco de reserva.</p>
<p class="justify">1 –   Para modalidade CNPq</p>
<p class="justify">1.1 O discente não entregará documentação, porém deverá possuir:</p>
<p class="justify">a . Conta Corrente no Banco do Brasil;</p>
<p class="justify">b . Currículo Lattes atualizado na Plataforma Chagas;</p>
<p class="justify">1.1.2. No início de agosto o CNPq enviará um email para o bolsista solicitando a emissão on line do aceite da bolsa. O email só será enviado para os discentes que tiverem o currículo Lattes atualizado na Plataforma Chagas. Caso haja algum problema com o currículo do bolsita haverá redução no período de vigência da bolsa.</p>
<p class="justify">2- Para modalidade ICBT</p>
<p class="justify">a. Extrato de conta bancária de qualquer banco, contendo números de agência e conta corrente;</p>
<p class="justify">b. Declaração do discente de que disponibilizará de 20h para desenvolver as atividades do projeto de pesquisa e de que não possui vínculo empregatício ou qualquer outro tipo de bolsa</p>
<p class="justify">c. Fotocópia do CPF e do RG do candidato</p>
<p class="justify">d. Histórico escolar com CRAA impresso e assinado;</p>
<p class="justify">e. Comprovante de residência (Para modalidade ICB);</p>
<p class="justify">f. Comprovante de matrícula do bolsista;</p>
<p class="justify">2.1 Comparecer à Gerência de Recursos Humanos (GERHU) portando carteira de trabalho para assinatura do Termo de Compromisso.  O aluno que não comparecer no período solicitado terá a bolsa implementada posteriormente, sem direito a pagamento retroativo.</p>
<p class="justify">2.2 Comparecer ao NIT junto com o orientador para assinatura do Termo de Aceite da bolsa.</p>

